// Java is a statically-typed language, which means you must declare the variable type when creating one.
// Variables are used to store data for processing. Here are some basic types:

/*
- Integer Types
    - byte (1 byte)
    - short (2 bytes)
    - int (4 bytes)
    - long (8 bytes)
- Floating-Point Types
    - float (4 bytes)
    - double (8 bytes)
- Character Types
    - char (2 bytes)
- Boolean Types
    - boolean (1 bit)
 */

// Example of how to declare variables in Java:

public class gpt2 {
    public static void main(String[] args) {
        int a = 5; // declaring an integer
        double b = 5.5; // declaring a double
        char c = 'c'; // declaring a character
        boolean d = true; // declaring a boolean
        String e = "Hello, World!"; // declaring a string

        System.out.println("Integer: " + a);
        System.out.println("Double: " + b);
        System.out.println("Character: " + c);
        System.out.println("Boolean: " + d);
        System.out.println("String: " + e);
    }
}

// This code declares five variables of different types and prints them out.


